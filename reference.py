def add_numbers(num1, num2):
    sum = num1 + num2
    print("Sum: ",sum)

add_numbers(5, 4)

def sub_numbers(num1, num2):
    sum = num1 - num2
    print("Minuend: ",sum)

sub_numbers(5, 4)

def divide_numbers(num1, num2):
    sum = num1 / num2
    print("Quotient: ",sum)

divide_numbers(5, 4)

def multiply_numbers(num1, num2):
    sum = num1 * num2
    print("Product: ",sum)

multiply_numbers(5, 4)

def square_root_number(num1):
    sum = num1 ** (1/2)
    print("Square Root: ",sum)

square_root_number(4)

def cube_root_number(num1):
    sum = num1 ** (1/3)
    print("Cube Root: ",sum)

cube_root_number(8)

def inverse_exp_number(num1):
    sum = num1 ** (-1)
    print("Inverse Exponent: ",sum)

inverse_exp_number(5)
